using UnityEngine;

public class YAxisAudioRange : MonoBehaviour
{
    // ????????????????????
    private float distance;

    // ???????????
    public float maxDistance = 10f;

    // ???AudioSource???????
    private AudioSource audioSource;

    // ??????????????????????????
    public float listenerHeight;

    // Start is called before the first frame update
    void Start()
    {
        // AudioSource??????????
        audioSource = GetComponent<AudioSource>();

        // ????????????
        listenerHeight = Camera.main.transform.position.y;
    }

    // Update is called once per frame
    void Update()
    {
        // ??????????????????????????
        Vector3 listenerPosition = Camera.main.transform.position;

        // ??????????????y???????????
        distance = Mathf.Abs(transform.position.y - listenerPosition.y);

        // ????????
        if (distance <= maxDistance)
        {
            audioSource.volume = 1f - (distance / maxDistance);
        }
        else
        {
            audioSource.volume = 0f;
        }
    }
}
