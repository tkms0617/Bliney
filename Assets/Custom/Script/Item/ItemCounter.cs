using UnityEngine;
using TMPro;

public class DecreaseNumberOnSpace : MonoBehaviour
{
    public TMP_Text targetText; // ?????TextMeshPro????

    private void Update()
    {
        // ??????????????????
        if (Input.GetKeyDown(KeyCode.Space))
        {
            DecreaseNumber();
        }
    }

    private void DecreaseNumber()
    {
        // ????????????1????
        int currentNumber;
        if (int.TryParse(targetText.text, out currentNumber))
        {
            currentNumber--;

            // ???0????????????
            currentNumber = Mathf.Max(0, currentNumber);

            // ?????????????
            targetText.text = currentNumber.ToString();
        }
        else
        {
            Debug.LogError("???????????????????");
        }
    }
}
